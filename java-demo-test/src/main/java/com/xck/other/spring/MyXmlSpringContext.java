    public final String TAG_CONTEXT_COMPONENT_SCAN = "context:component-scan";
    public final String TAG_CONTEXT_ANNOTATION_CONFIG = "context:annotation-config";

    private String configPath;

    public MyXmlSpringContext(String configPath){
        this.configPath = configPath;
        try {
            init();
        } catch (Exception e) {
            throw new RuntimeException(e);
        }
    }

    private void init() throws Exception{
        URL configUrl = MyXmlSpringContext.class.getClassLoader().getResource(configPath);
        if (configUrl == null) {
            throw new RuntimeException("config file not found");
        }

        File file = new File(configUrl.getFile());
        Document document = Jsoup.parse(file, "UTF-8");

        Elements componentScanEle = document.getElementsByTag(TAG_CONTEXT_COMPONENT_SCAN);
        if (componentScanEle.size() > 0) {
            String basePackage = componentScanEle.get(0).attr("base-package");
            ComponentScan componentScan = new ComponentScan();
            componentScan.scan(basePackage);
        }
    }

