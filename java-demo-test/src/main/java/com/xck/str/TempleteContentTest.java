    /**
     * 匹配测试
     * @param len
     * @param type
     * @return
     */
    public static long testMatch(int len, int type, int times, Pattern templete) {
        long useTimes = 0;
        for (int i=0; i<times; i++) {
            String content = randomString(len, type);
            long start = System.currentTimeMillis();
            match(content, templete);
            useTimes += System.currentTimeMillis() - start;
        }
        return useTimes;
    }

    /**
     * 随机生成三种类型的字符串，英文+数字，纯中文(0x4e00-0x9fa5)，中文+其他(utf8编码0x0000-0x9fa5)，字符串长度是343
     * 调用n次，每次循环m次，循环每次都会拿到一个字符串，匹配固定的模板.*【Keep】.*
     *
     * 1. 英文+数字，调用20次，每次循环10000次，每次调用耗时5646ms
     * 2. 英文+数字，调用10次，每次循环10000次，每次调用耗时5792ms
     * 3. 英文+数字，调用10次，每次循环1200次，每次调用耗时755ms，平均0.62ms
     *
     * 4. 中文+其他，调用20次，每次循环10000次，每次调用耗时5330ms
     * 5. 纯中文，调用20次，每次循环10000次，每次调用耗时7744ms
     *
     * 模板缓存：.*【昆山平安驾驶员培训股份有限公司】.*
     * 1. 英文+数字，调用10次，每次循环2000次，每次调用耗时1224ms，平均0.6ms
     *
     * @param content
     * @return
     */
    public static boolean match(String content, Pattern pattern) {
        content = content.replace("\\s", "");
        Matcher matcher = pattern.matcher(content);
        return matcher.find();
    }


    /**
     * 随机生成字符串
     * @param len 生成字符串的长度
     * @param type 默认英文+数字；1-中文+数字
     * @return
     */
    public static String randomString(int len, int type){
        if(1 == type){
            return RandomStringUtils.random(len, 0x4e00, 0x9fa5, true,true);
        }else {
            return RandomStringUtils.random(len, true,true);
        }
    }

